function report = run_high_throughput_evaluation
%RUN_HIGH_THROUGHPUT_EVALUATION Evaluate the paper's T=0 decoding mode.
%   The Default reference profile is used. Both architectures occupy 256
%   HT-Data symbols (128 tag slots): 2x2x2 carries 256 bits and 2x4x4
%   carries 512 bits, matching the prototype's 250/500 kbps raw rates.

repoRoot = fileparts(mfilename('fullpath'));
oldPath = path;
cleanup = onCleanup(@() path(oldPath));
restoredefaultpath;
addpath(fullfile(repoRoot, '802.11nHT'));

report = run_reference_profile_comparison( ...
    'Profile', 'Default', ...
    'Mode', 'HighThroughput', ...
    'NumDataSymbols', 256, ...
    'SNR', 2:10, ...
    'MinBits', 1e5, ...
    'MaxBits', 2e6, ...
    'TargetErrors', 200, ...
    'Plot', true, ...
    'SaveResults', true);

disp(report.comparison);
disp(report.rateTable);
fprintf('Raw tag rates: 2x2x2 %.0f kbps; 2x4x4 %.0f kbps.\n', ...
    report.rateTable.RawTagRateKbps(1), ...
    report.rateTable.RawTagRateKbps(2));
fprintf('Mean 2x4x4/2x2x2 net-goodput gain: %.3fx.\n', ...
    report.meanGoodputGain244);
clear cleanup
end
